export const calculateDistance = (lat1, lng1, lat2, lng2) => {
  const R = 6371;
  const dLat = ((lat2 - lat1) * Math.PI) / 180;
  const dLng = ((lng2 - lng1) * Math.PI) / 180;
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos((lat1 * Math.PI) / 180) * Math.cos((lat2 * Math.PI) / 180) *
    Math.sin(dLng / 2) * Math.sin(dLng / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  return Math.round((R * c) * 10) / 10;
};

export const calculateFare = (distance, transportType) => {
  const transport = TRANSPORT_TYPES.find(t => t.id === transportType);
  if (!transport) return 0;
  return Math.round(transport.baseRate + distance * transport.ratePerKm);
};

export const estimateTime = (distance, transportType) => {
  const speeds = { bus: 25, cng: 30, rickshaw: 15, tempo: 20, leguna: 22, launch: 12 };
  const speed = speeds[transportType] || 20;
  return Math.round((distance / speed) * 60);
};
